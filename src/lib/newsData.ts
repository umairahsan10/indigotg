// Helper function to generate slug from title
export const generateSlug = (title: string): string => {
    return title
        .toLowerCase()
        .replace(/[^a-z0-9\s-]/g, '')
        .replace(/\s+/g, '-')
        .replace(/-+/g, '-')
        .trim();
};

// Helper function to find article by slug
export const findArticleBySlug = (slug: string) => {
    return newsData.flat().find(item => 
        item.slug === slug || 
        generateSlug(item.title) === slug
    );
};
